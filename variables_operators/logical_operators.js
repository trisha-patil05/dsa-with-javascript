/**
 * =============================================
 * 📌 Topic     : Operators - Logical
 * 🔢 Problem   : Logical Operators ( &&, || )
 * ⚡ Difficulty : Easy
 * =============================================
 * 🧪 Examples:
 *  true && false → false
 *  true || false → true
 *
 * ⏱️ Time : O(1) | 🧠 Space : O(1)
 * =============================================
 */

let x = true, y = false;

console.log("AND (&&) :", x && y);  // false
console.log("OR  (||) :", x || y);  // true
console.log("NOT (!)  :", !x);      // false