/**
 * =============================================
 * 📌 Topic     : For Loop
 * 🔢 Problem   : Print N Hello Worlds
 * ⚡ Difficulty : Easy
 * =============================================
 * 🧪 Examples:
 *  Input : 3 → "Hello World" printed 3 times
 *  Input : 0 → nothing printed
 *
 * ⏱️ Time : O(n) | 🧠 Space : O(1)
 * =============================================
 */

let n = 5;

for (let i = 1; i <= n; i++) {
    console.log("Hello World");
}