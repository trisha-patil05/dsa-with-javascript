/**
 * =============================================
 * 📌 Topic     : Do-While Loop
 * 🔢 Problem   : Repeat Hello
 * ⚡ Difficulty : Easy
 * =============================================
 * 🧪 Examples:
 *  Input : 3 → Hello (x3)
 *
 * ⏱️ Time : O(n) | 🧠 Space : O(1)
 * =============================================
 */

let n = 3;
let i = 1;

do {
  console.log("Hello");
  i++;
} while (i <= n);