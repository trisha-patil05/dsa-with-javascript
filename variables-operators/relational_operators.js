/**
 * =============================================
 * 📌 Topic     : Operators - Relational
 * 🔢 Problem   : Relational Operators ( >, <, >=, <=, != )
 * ⚡ Difficulty : Easy
 * =============================================
 * 🧪 Examples:
 *  10 > 3  → true
 *  10 <= 3 → false
 *
 * ⏱️ Time : O(1) | 🧠 Space : O(1)
 * =============================================
 */

let a = 10, b = 3;

console.log("a > b  :", a > b);   // true
console.log("a < b  :", a < b);   // false
console.log("a >= b :", a >= b);  // true
console.log("a <= b :", a <= b);  // false
console.log("a != b :", a != b);  // true